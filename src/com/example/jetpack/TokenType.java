package com.example.jetpack;

public enum TokenType {
    LBRACE,       // {
    RBRACE,       // }
    COLON,        // :
    SEMICOLON,    // ;
    LBRACKET,     // [
    RBRACKET,     // ]
    STRING,       // "string"
    NUMBER,       // 123
    BOOLEAN,      // true/false
    IDENTIFIER,   // e.g., name, age
    COMMA         // ,
}
